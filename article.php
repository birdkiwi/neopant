                        <p>
                            На протяжении уже нескольких тысяч лет в Японии, Китае и Корее лечат множество болезней с помощью средств, главным компонентом которых являются панты марала. В фольклоре очень многих народов олень часто связывается с Востоком, восходом солнца, новизной и созиданием. Взрослый самец оленя является символом изобилия, а ветвистые рога его воспринимаются как солнечные лучи и древо жизни, несут идею возрождения, так как на месте старых рогов регулярно появляются новые.  Но уже с 1892 г. уцелевших оленей на Алтае стали отлавливать и создавать для них заповедники, и в начале прошлого столетия пантовое оленеводство в России прочно встало на ноги. На сегодняшний день наша страна являемся одним из крупнейших экспортеров пантов алтайского марала, а главный наш импортер — Корея.
                        </p>
                        <p>
                            Пантовая продукция– одна из самых эффективных в мире, что напрямую отражается в цене на мировом рынке: панты алтайского марала дороже всех остальных в полтора-два раза, так как их лечебный эффект несопоставимо выше.
                        </p>
                        <p>
                            А с 1996 года начался принципиально новый этап в производстве препаратов из пантов марала. Главный их компонент – это кровь марала, которую можно было собирать исключительно во время резки пантов (это происходит в мае-июле), так как именно тогда она наиболее насыщена биологически активными веществами. Длительному же хранению кровь марала подвергать нельзя, поэтому лечебные процедуры (в т. ч. ванны) могли носить только сезонный характер. Но российские ученые эту проблему решили: был разработан сухой пантогематоген – средство, созданное на основе крови из пантов, подвергшейся низкотемпературной дегидратации и стерилизованной. Это вещество можно хранить достаточно долго, что дало возможность проводить лечение круглогодично.
                        </p>
